#1. Arithmetic Operations
#a. addition
import numpy as np
arr1 = np.array([1, 5, 6, 7])
arr2 = np.array([5,46,78,65])
print(np.add(arr1,arr2))

#b. substraction

arr1 = np.array([56,78,99])
arr2 = np.array([45,12,34])
print(np.subtract(arr1,arr2))

#3. multiply

arr1 = np.array([4,5,2])
arr2 = np.array([1,4,7])
print(np.multiply(arr1,arr2))

#4. divide

arr1 = np.array([45,9])
arr2 = np.array([9,3])
print(np.divide(arr1,arr2))
